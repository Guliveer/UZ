# Korzystając z dokumentacji Pythona dotyczącej zmiennych podstawowych
# napisać program wyświetlający liczbę całkowitą w postaci ósemkowej, szesnastkowej i dwójkowej.

a = 10
print("Liczba całkowita: ", a)
print("Liczba w postaci ósemkowej: ", oct(a))
print("Liczba w postaci szesnastkowej: ", hex(a))
print("Liczba w postaci dwójkowej: ", bin(a))
