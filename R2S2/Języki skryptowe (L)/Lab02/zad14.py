# Podać cztery podstawowe (wbudowane) typy danych Pythona

def explain_basic_data_types():
    explanation = """
    Cztery podstawowe (wbudowane) typy danych w Pythonie to:

    1. int (liczby całkowite) - reprezentuje liczby całkowite, np. 1, 2, 3.
    2. float (liczby zmiennoprzecinkowe) - reprezentuje liczby rzeczywiste, np. 1.0, 2.5, 3.14.
    3. str (łańcuchy znaków) - reprezentuje tekst, np. "Hello, World!".
    4. bool (wartości logiczne) - reprezentuje wartości logiczne, np. True, False.

    Przykłady użycia:
    a = 10  # int
    b = 3.14  # float
    c = "Hello, World!"  # str
    d = True  # bool

    print(f"a: {a}, typ: {type(a)}")
    print(f"b: {b}, typ: {type(b)}")
    print(f"c: {c}, typ: {type(c)}")
    print(f"d: {d}, typ: {type(d)}")
    """
    print(explanation)

explain_basic_data_types()
