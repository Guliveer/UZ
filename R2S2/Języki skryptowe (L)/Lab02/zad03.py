# Napisać program, który wyświetli wynik dodawania, mnożenia, dzielenia, odejmowania i potęgowania
# dwóch liczb przypisanych do zmiennych a, b.

a = 5
b = 3

print(f"Dodawanie: {a + b}")
print(f"Mnożenie: {a * b}")
print(f"Dzielenie: {a / b}")
print(f"Odejmowanie: {a - b}")
print(f"Potęgowanie: {a ** b}")
