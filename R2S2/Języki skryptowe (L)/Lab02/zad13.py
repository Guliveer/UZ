# Co to jest kod źródłowy?

def explain_source_code():
    explanation = """
    Kod źródłowy to zbiór kodu napisanego w języku programowania zrozumiałym dla człowieka.
    Jest to oryginalny kod, który programista pisze, a który następnie jest kompilowany lub interpretowany
    w celu stworzenia programu wykonywalnego. Kod źródłowy jest niezbędny do zrozumienia, utrzymania
    i modyfikacji oprogramowania.

    Przykład prostego kodu źródłowego w Pythonie:
    def greet():
        print("Hello, World!")

    greet()
    """
    print(explanation)

explain_source_code()
