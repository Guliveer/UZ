# W jaki sposób można modyfikować łańcuch znaków?

# Original string
original_string = "Hello, World!"

# Concatenation
concatenated_string = original_string + " How are you?"

# Replacement
replaced_string = original_string.replace("World", "Python")

# Uppercase
uppercase_string = original_string.upper()

# Lowercase
lowercase_string = original_string.lower()

# Slicing
sliced_string = original_string[7:12]

# Displaying the results
print("Original String:\t", original_string)
print("Concatenated String:", concatenated_string)
print("Replaced String:\t", replaced_string)
print("Uppercase String:\t", uppercase_string)
print("Lowercase String:\t", lowercase_string)
print("Sliced String:\t\t", sliced_string)
