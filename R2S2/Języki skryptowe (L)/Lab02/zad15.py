# Dlaczego te typy nazywane są podstawowymi (wbudowanymi)?

def explain_why_basic_data_types():
    explanation = """
    Te typy danych nazywane są podstawowymi (wbudowanymi), ponieważ są integralną częścią języka Python.
    Są one dostępne bez potrzeby importowania dodatkowych modułów i są fundamentalne dla większości operacji
    wykonywanych w Pythonie. Dzięki nim można wykonywać podstawowe operacje arytmetyczne, manipulować tekstem,
    oraz przechowywać wartości logiczne.

    Przykłady użycia:
    a = 10  # int
    b = 3.14  # float
    c = "Hello, World!"  # str
    d = True  # bool

    print(f"a: {a}, typ: {type(a)}")
    print(f"b: {b}, typ: {type(b)}")
    print(f"c: {c}, typ: {type(c)}")
    print(f"d: {d}, typ: {type(d)}")
    """
    print(explanation)

explain_why_basic_data_types()
