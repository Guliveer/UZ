# Co to jest interpreter Pythona?

def explain_python_interpreter():
    explanation = """
    Interpreter Pythona to program, który wykonuje kod napisany w języku Python.
    Działa on poprzez analizowanie i wykonywanie instrukcji zawartych w kodzie źródłowym Pythona.
    Interpreter Pythona może być uruchamiany w trybie interaktywnym, gdzie użytkownik wpisuje
    polecenia bezpośrednio i otrzymuje natychmiastowe wyniki, lub w trybie wsadowym, gdzie
    wykonuje skrypty zapisane w plikach.

    Przykład uruchomienia interpretera Pythona w trybie interaktywnym:
    $ python
    >>> print("Hello, World!")
    Hello, World!

    Przykład uruchomienia skryptu Pythona w trybie wsadowym:
    $ python script.py
    """
    print(explanation)

explain_python_interpreter()
