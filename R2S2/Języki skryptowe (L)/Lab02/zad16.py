# Czym jest sekwencja?

def explain_sequence():
    explanation = """
    Sekwencja to uporządkowana kolekcja elementów, które mogą być indeksowane i iterowane.
    W Pythonie sekwencje obejmują typy takie jak listy, krotki, łańcuchy znaków i zakresy.
    Sekwencje pozwalają na dostęp do elementów za pomocą indeksów oraz na wykonywanie operacji
    takich jak cięcie, konkatenacja i iteracja.

    Przykłady sekwencji w Pythonie:
    lista = [1, 2, 3, 4, 5]  # lista
    krotka = (1, 2, 3, 4, 5)  # krotka
    lancuch = "Hello, World!"  # łańcuch znaków
    zakres = range(5)  # zakres

    print(f"Lista: {lista}")
    print(f"Krotka: {krotka}")
    print(f"Łańcuch: {lancuch}")
    print(f"Zakres: {list(zakres)}")
    """
    print(explanation)

explain_sequence()
