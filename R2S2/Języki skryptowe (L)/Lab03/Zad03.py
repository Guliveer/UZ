# Jaka jest różnica pomiędzy while a for?

# Przykład użycia while (Nieokreślona liczba iteracji)
print("While loop:")
i = 0
while i < 5: # Pętla będzie wykonywana dopóki i < 5
    print(i)
    i += 1
# Wynik: 0 1 2 3 4

print()

# Przykład użycia for (Określona liczba iteracji)
print("For loop:")
for i in range(5): # Pętla będzie wykonywana dla i = 0, 1, 2, 3, 4
    print(i)
# Wynik: 0 1 2 3 4